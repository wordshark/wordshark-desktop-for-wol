    private JCheckBox[] getCheckBoxes(JPanel jp) {
        Component c[] = jp.getComponents();
        JCheckBox retc[] = new JCheckBox[]{};
        for (int i = 0; i < c.length; i++) {
            if (c[i] instanceof JCheckBox) {
                retc = u.addJCheckBox(retc, (JCheckBox) c[i]);
            }
        }
        return retc;
    }

    private void doSelectAllCheck(JPanel jp, JButton jb) {
        JCheckBox c[] = getCheckBoxes(jp);
        boolean allselected = true;
        for (int i = 0; i < c.length; i++) {
            if (!c[i].isSelected()) {
                allselected = false;
            }
        }
        if (allselected) {
            jb.setText(deselectall);
        } else {
            jb.setText(selectall);
        }
    }

    private void doSelectAll(JPanel jp, JButton jb) {
        boolean toSelect = jb.getText().equalsIgnoreCase(selectall);
        JCheckBox c[] = getCheckBoxes(jp);
        for (int i = 0; i < c.length; i++) {
            c[i].setSelected(toSelect);
        }
        doSelectAllCheck(jp, jb);
    }

